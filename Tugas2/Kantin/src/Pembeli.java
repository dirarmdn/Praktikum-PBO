public class Pembeli {
    private String nama;

    // Konstruktor untuk inisialisasi nama
    public Pembeli(String nama) {
        this.nama = nama;
    }

    // Getter untuk nama
    public String getNama() {
        return nama;
    }

    // Setter untuk nama
    public void setNama(String nama) {
        this.nama = nama;
    }
}
